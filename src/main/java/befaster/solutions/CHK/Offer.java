package befaster.solutions.CHK;

public class Offer {

	private int count;
	private int price;

	public Offer(final int count, final int price) {
		this.count = count;
		this.price = price;
	}

	public int getCount() {
		return count;
	}

	public void setCount(final int count) {
		this.count = count;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(final int price) {
		this.price = price;
	}
}
