package befaster.solutions.CHK;

public class Offer {

	private Character sku;
	private int price;

	public Character getSku() {
		return sku;
	}

	public void setSku(final Character sku) {
		this.sku = sku;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(final int price) {
		this.price = price;
	}
}
